    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        if (btnthem.getText().equals("Thêm")) {           
            statement = true;
            functionSelected = 0;
            setStatement(statement);
            
        }
        else{
            loadDatatoTable();
            statement = false;
            setStatement(statement);
            functionSelected = -1;
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btnluuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnluuActionPerformed
        
        if (txtma.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Mã NXB không được để trống");
            //loadDatatoTable();
            return;
        }
        Publisher pub = new Publisher(txtma.getText(),txtten.getText(),txtadd.getText(),txtsdt.getText());
        if (functionSelected == 0) {
            PublisherModify.insertPublisher(pub);
            
        }
        else if (functionSelected == 1) {
            PublisherModify.updatePublisher(pub);
        }
        statement = false;
        setStatement(statement);
        functionSelected = -1;
        loadDatatoTable();
        
    }//GEN-LAST:event_btnluuActionPerformed

    private void btnsuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsuaActionPerformed
        if (btnsua.getText().equals("Sửa")) {
            functionSelected = 1;
            statement = true;
            setStatement(statement);
            
        }
        else{
            loadDatatoTable();
            statement = false;
            setStatement(statement);
            functionSelected = -1;
        }
    }//GEN-LAST:event_btnsuaActionPerformed

    private void btnxoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnxoaActionPerformed
        if (txtma.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null,"Bạn phải điền vào mã NXB!");
        }
        else
        {
            int rs = JOptionPane.showConfirmDialog(null,"Bạn muốn xóa chứ ?","Xóa",JOptionPane.YES_NO_OPTION);
            if (rs == 0) {
                String id = txtma.getText().trim();
                PublisherModify.deletePublisher(id);
                loadDatatoTable();
            }
        }
    }//GEN-LAST:event_btnxoaActionPerformed

