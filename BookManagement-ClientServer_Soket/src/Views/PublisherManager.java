    public void loadDatatoTable(){
        DefaultTableModel defaultTable = (DefaultTableModel) jTable1.getModel();
        defaultTable.setRowCount(0);
        
        ArrayList<Publisher> pubList = PublisherModify.getAllPublisher();
        if (pubList.size() > 0) {
            for (Publisher publisher : pubList) {
                defaultTable.addRow(new Object[] {publisher.getId(),publisher.getName(),
                    publisher.getAddress(),publisher.getPhoneNumber()});
            }
            jTable1.setRowSelectionInterval(0,0);
        }
    }
    
    public void selectionRowChange(){
        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent lse) {
                int i = jTable1.getSelectedRow();
                if (i >= 0) {
                    txtma.setText(jTable1.getModel().getValueAt(i, 0).toString());
                    txtten.setText(jTable1.getModel().getValueAt(i, 1).toString());
                    txtadd.setText(jTable1.getModel().getValueAt(i, 2).toString());
                    txtsdt.setText(jTable1.getModel().getValueAt(i, 3).toString());
                    labelCountItemIndexInList();
                }
            }
        });
    }
    //lable đếm số hàng trên tổng hàng
    public void labelCountItemIndexInList(){
        String currentIndex = String.valueOf(jTable1.getSelectedRow()+1);
        String rowCount = String.valueOf(jTable1.getRowCount());   
        lblcount.setText(currentIndex+"/"+rowCount);
    }
    
    public void setStatement(boolean bl){
      btnluu.setEnabled(statement);
      btnpre.setEnabled(!statement);
      btnnext.setEnabled(!statement);
      txtma.setEditable(statement);
      txtten.setEditable(statement);
      txtadd.setEditable(statement);
      txtsdt.setEditable(statement);
      lbltb.setText(" ");
        if (functionSelected == 0) {
            if (bl) {
                btnthem.setText("Hủy");
                Icon icon = new ImageIcon("src\\Icon\\reject.png");
                btnthem.setIcon(icon);
                txtma.setText("");
                txtten.setText("");
                txtadd.setText("");
                txtsdt.setText("");
                txtma.requestFocus();
            }
             else{
                btnthem.setText("Thêm");
                Icon icon = new ImageIcon("src\\Icon\\add.png");
                btnthem.setIcon(icon);
            }
            
            btnsua.setEnabled(!statement);
        }
        else if (functionSelected == 1) {
            if (bl) {
                btnsua.setText("Hủy");
                Icon icon = new ImageIcon("src\\Icon\\reject.png");
                btnsua.setIcon(icon);
                txtten.requestFocus();
            }
            else{
                btnsua.setText("Sửa");
                Icon icon = new ImageIcon("src\\Icon\\pencil.png");
                btnsua.setIcon(icon);
                
            }
            txtma.setEnabled(!statement);
            btnthem.setEnabled(!statement);
        }
      
      btnxoa.setEnabled(!statement);
      
    }
    
    
